class Torrent extends Ability {
    constructor() {
        super("Torrent", 0, 0, 0, 0, false, false, false, "physical", 5, 1)
        this.passive = true
        this.talent = true
        this.talentSelect = true
    }

    getTooltip() {
        return "Increases the initial heal from Riptide by 20%."
    }

    setTalent(caster) {
        caster.abilities["Riptide"].spellPower *= 1.2
    }

    unsetTalent(caster) {
        caster.abilities["Riptide"].spellPower /= 1.2
    }
}
